const readline = require("readline");
const {
    normalizeMongoUri,
    quoteEnvValue,
    readMongoUriFromEnvContents,
} = require("./mongoUri");

const DEFAULT_CLIENT_URL = "https://aluxie07.github.io/IWP-HomeShare";

function escapePowerShellSingleQuoted(value) {
    return String(value).replace(/'/g, "''");
}

function buildEnvFile({ mongoUri, adminEmail, jwtSecret }) {
    const lines = [
        "# HomeShare local server — generated by setup wizard",
        "FILE_STORAGE=disk",
        "SKIP_RECAPTCHA=true",
        "PORT=8080",
        `CLIENT_URL=${DEFAULT_CLIENT_URL}`,
        `ALLOWED_ORIGINS=${DEFAULT_CLIENT_URL},http://localhost:3000,http://127.0.0.1:3000,http://localhost:8080,http://127.0.0.1:8080`,
        `JWT_SECRET=${jwtSecret}`,
        `MONGO_URI=${quoteEnvValue(normalizeMongoUri(mongoUri))}`,
        "",
    ];

    if (adminEmail) {
        lines.push(`ADMIN_EMAIL=${adminEmail}`);
        lines.push(`LOCAL_SYNC_OWNER_EMAIL=${adminEmail}`);
        lines.push("");
    } else {
        lines.push("# Optional — make this email an admin:");
        lines.push("# ADMIN_EMAIL=you@example.com");
        lines.push("");
    }

    lines.push("# Optional email (Brevo API):");
    lines.push("# BREVO_API_KEY=xkeysib-...");
    lines.push("# EMAIL_FROM=your-verified-sender@example.com");
    lines.push("");

    return lines.join("\n");
}

function needsSetup(envPath) {
    if (!fs.existsSync(envPath)) {
        return true;
    }
    const contents = fs.readFileSync(envPath, "utf8");
    if (contents.includes("USER:PASSWORD@cluster")) {
        return true;
    }
    if (contents.includes("change-this-to-a-long-random-string")) {
        return true;
    }
    const mongo = readMongoUriFromEnvContents(contents);
    if (!mongo || mongo.includes("USER:PASSWORD")) {
        return true;
    }
    return false;
}

