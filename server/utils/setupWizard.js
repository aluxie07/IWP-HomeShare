function ask(question) {
    const rl = readline.createInterface({
        input: process.stdin,
        output: process.stdout,
    });
    return new Promise((resolve) => {
        rl.question(question, (answer) => {
            rl.close();
            resolve(String(answer || "").trim());
        });
    });
}

async function runConsoleWizard() {
    console.log("");
    console.log("  HomeShare Local Server — first-time setup");
    console.log("  =========================================");
    console.log("");

    let mongoUri = "";
    while (!mongoUri || mongoUri.includes("USER:PASSWORD")) {
        mongoUri = await ask("  MongoDB URI: ");
        if (!mongoUri || mongoUri.includes("USER:PASSWORD")) {
            console.log("  Please paste your real mongodb+srv://... connection string.");
        }
    }

    const adminEmail = await ask("  Admin email (optional, Enter to skip): ");
    return { mongoUri, adminEmail };
}

/**
 * Shows a setup UI (Windows popup when possible) and writes .env.
 * Returns true if setup completed, false if user cancelled.
 */
async function runSetupWizard(envPath) {
    let answers = null;

    if (process.platform === "win32") {
        console.log("  Opening setup window…");
        answers = runWindowsGuiWizard();
    }

    if (!answers) {
        answers = await runConsoleWizard();
    }

    if (!answers?.mongoUri || answers.mongoUri.includes("USER:PASSWORD")) {
        return false;
    }

    const mongoUri = normalizeMongoUri(answers.mongoUri);
    const jwtSecret = crypto.randomBytes(32).toString("hex");
    const contents = buildEnvFile({
        mongoUri,
        adminEmail: answers.adminEmail,
        jwtSecret,
    });

    fs.mkdirSync(path.dirname(envPath), { recursive: true });
    fs.writeFileSync(envPath, contents, "utf8");

    const saved = fs.readFileSync(envPath, "utf8");
    if (!saved.includes(mongoUri)) {
        console.error(`  Could not write config to ${envPath}`);
        return false;
    }

    console.log(`  Saved config: ${envPath}`);
    return true;
}

